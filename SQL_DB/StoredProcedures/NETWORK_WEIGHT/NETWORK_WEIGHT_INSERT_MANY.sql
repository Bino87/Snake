CREATE PROCEDURE [dbo].[NETWORK_WEIGHT_INSERT_MANY]
	@DATA_TABLE [dbo].NETWORK_WEIGHT_TYPE READONLY
AS
BEGIN
	DECLARE @RETURN_VALUE [dbo].NETWORK_WEIGHT_TYPE

	INSERT INTO [dbo].NETWORK_WEIGHT
	OUTPUT inserted.ID, inserted.INTERNAL_INDEX, inserted.LAYER_ID, inserted.VALUE INTO @RETURN_VALUE
	SELECT INTERNAL_INDEX, LAYER_ID, VALUE FROM @DATA_TABLE

	SELECT * FROM @RETURN_VALUE
;
END
RETURN 0
